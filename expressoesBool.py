# Escreva as seguintes expressões booleanas em linguagem Python:

# O somatório de 2 com 2 é menor do que 4:
print((2 + 2) < 4) #false

# O valor de 7 // 3 é igual a 1 + 1:
print((7 // 3) == (1 + 1)) #true

# A soma de 3 elevado ao quadrado com 4 elevado ao quadrado é igual a 25:
print((3**2) + (4**2) == 25) #true

# A soma de 2, 4 e 6 é maior do que 12:
print(2 + 4 + 6 > 12) #false

# 1387 é divisível por 19:
print(1387 % 19 == 0) #true

# 31 é par:
print(31 % 2 == 0) #false

# O menor valor entre: 34, 29 e 31 é menor que 30
print(min(34,29,31) < 30) #true